// Function to update text content
function updateTextContent(element, key, lang) {
    if (translations[lang] && translations[lang][key]) {
        if (element.tagName === 'INPUT' && element.type !== 'submit') {
            element.placeholder = translations[lang][key];
        } else {
            element.textContent = translations[lang][key];
        }
    }
}

// Function to get the current page name
function getCurrentPageName() {
    const path = window.location.pathname;
    const page = path.split('/').pop();
    return page || 'index.html';
}

// Function to get the corresponding language page
function getLanguagePage(currentPage, targetLang) {
    if (targetLang === 'fr') {
        if (currentPage === 'index.html' || currentPage === '') {
            return 'index_fr.html';
        }
        return currentPage.replace('.html', '_fr.html');
    } else {
        if (currentPage === 'index_fr.html') {
            return 'index.html';
        }
        return currentPage.replace('_fr.html', '.html');
    }
}

// Function to switch language
function switchLanguage(lang) {
    const currentPage = getCurrentPageName();
    const targetPage = getLanguagePage(currentPage, lang);
    window.location.href = targetPage;
}

// Initialize language selector
document.addEventListener('DOMContentLoaded', function() {
    const languageLinks = document.querySelectorAll('[data-lang]');
    
    languageLinks.forEach(link => {
        link.addEventListener('click', function(e) {
            e.preventDefault();
            const lang = this.getAttribute('data-lang');
            switchLanguage(lang);
        });
    });
}); 